async function migrate() {
  console.log("Migrating new prompts and bot menu buttons...");

  for (const prompt of newPrompts) {
    const existing = await prisma.prompt.findFirst({
      where: { slug: prompt.slug, feature: prompt.feature },
    });
    if (existing) {
      await prisma.prompt.update({
        data: prompt,
        where: { id: existing.id },
      });
      console.log(`  Updated prompt: ${prompt.slug}`);
      continue;
    }
    await prisma.prompt.create({ data: prompt });
    console.log(`  Created prompt: ${prompt.slug}`);
  }

  for (const button of newButtons) {
    const existing = await prisma.botMenuButton.findFirst({
      where: { label: button.label },
    });
    if (existing) {
      console.log(`  Button "${button.label}" already exists, skipping.`);
      continue;
    }

    const nextButtons = await prisma.botMenuButton.findMany({
      orderBy: { sortOrder: "asc" },
      select: { id: true, sortOrder: true, label: true },
    });

    for (const b of nextButtons) {
      if (b.sortOrder >= button.sortOrder) {
        await prisma.botMenuButton.update({
          data: { sortOrder: b.sortOrder + 1 },
          where: { id: b.id },
        });
        console.log(`  Shifted "${b.label}" from sortOrder ${b.sortOrder} to ${b.sortOrder + 1}`);
      }
    }

    await prisma.botMenuButton.create({ data: button });
    console.log(`  Created button: ${button.label}`);
  }

  await prisma.$disconnect();
  console.log("Done.");
}

migrate().catch((error) => {
  console.error("Migration failed:", error);
  process.exit(1);
});